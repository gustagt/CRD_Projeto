    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Usuario usuario = new Usuario();
        
        usuario.setNomeCompleto(txfNome.getText());
        usuario.setEmail(txfEmail.getText());
        usuario.setSenha(passSenha.getText());
        usuario.setNomeUsuario(txfNomeUsuario.getText());
        usuario.setTelefone(Integer.parseInt(txfTelefone.getText()));
        
        
        if(usuario.getNomeCompleto().length()==0){
            JOptionPane.showMessageDialog(null,"O campo nome est√° vazio");
        }else if(usuario.getEmail().length()==0){
            JOptionPane.showMessageDialog(null,"O campo email est√° vazio");
        }else if(usuario.getSenha().length()<=7){
            JOptionPane.showMessageDialog(null,"O campo senha est√° preenchido errado");
        }else if(usuario.getNomeUsuario().length()==0){
            JOptionPane.showMessageDialog(null,"O campo nome de usuario est√° vazio");
        }else if(usuario.getTelefone()<=0){
            JOptionPane.showMessageDialog(null,"O campo telefone est√° vazio");
        }else {
            usuario.inserirUsario();    
        }      
    }//GEN-LAST:event_btCadastrarActionPerformed

